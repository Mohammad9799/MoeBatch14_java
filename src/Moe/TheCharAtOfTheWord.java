package Moe;

public class TheCharAtOfTheWord {
    public static void main(String[] args) {

        String word="parallelogram";

        char secondLitter=word.charAt(1);
        char fourthLitter=word.charAt(3);
        char sixthLitter=word.charAt(5);

        System.out.println(secondLitter+ " "+ fourthLitter+ " "+sixthLitter);


    }
}
